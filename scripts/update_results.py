"""Score every checkpoint + the classical baseline at both canvas sizes,
then write docs/results.md -- the project's living scoreboard.

Saves a per-checkpoint score.json so subsequent runs can skip recomputation
unless the checkpoint mtime is newer.
"""

from __future__ import annotations

import argparse
import json
import time
from pathlib import Path

from lines.eval.score_checkpoint import score_baseline, score_checkpoint


def _cache_path(ck_path: Path) -> Path:
    return ck_path.parent / "score.json"


def _is_cached(ck_path: Path) -> bool:
    cache = _cache_path(ck_path)
    if not cache.exists():
        return False
    return cache.stat().st_mtime >= ck_path.stat().st_mtime


def collect_results(checkpoint_root: Path, force: bool = False):
    results = []
    for ck in sorted(checkpoint_root.glob("*/model.pt")):
        if _is_cached(ck) and not force:
            data = json.loads(_cache_path(ck).read_text())
            print(f"[cached] {ck.parent.name}: score={data['report']['mean_score']:.3f}")
        else:
            print(f"[score]  {ck.parent.name} ...", flush=True)
            t0 = time.time()
            try:
                data = score_checkpoint(ck)
            except (RuntimeError, KeyError) as exc:
                # Skip non-SetPredictor checkpoints (e.g. autoregressive).
                # This scoreboard tracks the legacy set-predictor only.
                print(f"         (skipped: not a SetPredictor checkpoint -- {type(exc).__name__})")
                continue
            _cache_path(ck).write_text(json.dumps(data, indent=2, default=str))
            print(f"         score={data['report']['mean_score']:.3f}  ({time.time()-t0:.1f}s)")
        results.append(data)
    return results


def _row(name, canvas, score, iou, type_acc, geom, cov, line, arc, circle, n, note=""):
    def f(x, fmt="{:.3f}"):
        if x is None:
            return "n/a"
        return fmt.format(x)
    return (f"| {name} | {canvas} | **{f(score)}** | {f(iou)} | {f(type_acc)} | "
            f"{f(geom)} | {f(cov)} | {f(line)} | {f(arc)} | {f(circle)} | {n} | {note} |")


def write_markdown(results, baselines, out: Path):
    rows = []
    for b in baselines:
        r = b["report"]
        pc = r["per_class"]
        rows.append(("baseline", _row(
            f"`baseline`", b["canvas_side"], r["mean_score"], r["mean_render_iou"],
            r["mean_type_accuracy"], r["mean_geometric_error"], r["mean_coverage"],
            pc["line"]["geometric_error"], pc["arc"]["geometric_error"],
            pc["circle"]["geometric_error"], b["n_samples"], "classical"
        )))
    for d in results:
        r = d["report"]
        pc = r["per_class"]
        cfg = d.get("cfg", {})
        note_parts = []
        if "encoder_type" in d and d["encoder_type"]:
            note_parts.append(f"enc={d['encoder_type']}")
        if cfg.get("param_weight"):
            note_parts.append(f"pw={cfg['param_weight']}")
        if cfg.get("render_weight"):
            note_parts.append(f"rw={cfg['render_weight']}")
        if cfg.get("epochs"):
            note_parts.append(f"ep={cfg['epochs']}")
        rows.append(("model", _row(
            f"`{d['name']}`", d["canvas_side"], r["mean_score"], r["mean_render_iou"],
            r["mean_type_accuracy"], r["mean_geometric_error"], r["mean_coverage"],
            pc["line"]["geometric_error"], pc["arc"]["geometric_error"],
            pc["circle"]["geometric_error"], d["n_samples"], ", ".join(note_parts)
        )))
    # sort by score descending across both kinds
    score_of = {row[1]: row for row in rows}
    rows.sort(key=lambda x: -_extract_score(x[1]))

    header = (
        "| Run | Canvas | Score | RenderIoU | TypeAcc | GeomErr | Coverage | "
        "GeomErr (line) | GeomErr (arc) | GeomErr (circle) | N | Notes |\n"
        "|-----|--------|-------|-----------|---------|---------|----------|"
        "----------------|---------------|------------------|---|-------|"
    )

    out.parent.mkdir(parents=True, exist_ok=True)
    # Preserve any "## Current champion ..." headline section if it exists
    # (e.g. the autoregressive Stage 1 result lives there; this script only
    # auto-generates the legacy set-predictor scoreboard).
    headline = ""
    if out.exists():
        existing = out.read_text()
        sep = "\n## Legacy (set-predictor) scoreboard"
        if sep in existing:
            headline = existing.split(sep)[0].rstrip() + "\n\n"

    lines_md = []
    if headline:
        lines_md.append(headline)
        lines_md.append("## Legacy (set-predictor) scoreboard")
    else:
        lines_md.append("# Results scoreboard")
    lines_md.append("")
    lines_md.append("Auto-generated by `scripts/update_results.py`. "
                    "Higher is better for Score, RenderIoU, TypeAcc, Coverage; "
                    "lower is better for GeomErr.")
    lines_md.append("Per-class GeomErr is computed over Hungarian-matched pairs keyed on the GT type.")
    lines_md.append("")
    lines_md.append(header)
    lines_md.extend(row[1] for row in rows)
    lines_md.append("")
    out.write_text("\n".join(lines_md))


def _extract_score(row: str) -> float:
    # rows are formatted with the score as **0.xxx**
    import re
    m = re.search(r"\*\*([0-9.]+)\*\*", row)
    return float(m.group(1)) if m else 0.0


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--checkpoint-root", default="checkpoints")
    ap.add_argument("--out", default="docs/results.md")
    ap.add_argument("--force", action="store_true",
                    help="re-score every checkpoint even if cached score.json is fresh")
    args = ap.parse_args()

    results = collect_results(Path(args.checkpoint_root), force=args.force)
    baselines = [score_baseline(64), score_baseline(128)]
    write_markdown(results, baselines, Path(args.out))
    print(f"\nwrote {args.out}")


if __name__ == "__main__":
    main()
